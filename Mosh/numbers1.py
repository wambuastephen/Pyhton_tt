#!/usr/bin/env python3

import math

PI = 3.142

# Using the round() function to round the value of PI to the nearest integer
print(round(PI))  # Output: 3

# Using the abs() function to get the absolute value of PI
print(abs(PI))    # Output: 3.13

# Attempting to use math.floor() to get the floor value of PI (but not printing the result)
# This line will perform the operation but doesn't display the output
print(math.floor(PI))

