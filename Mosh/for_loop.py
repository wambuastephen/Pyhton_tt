#!/usr/bin/env python3
for x in "Python":
    print(x)
#for loop of numbers
my_list = [1,2,3,4,5]
for num in my_list:
    print(num)
#for loop of a string
my_string = "Hello"
for char in my_string:
    print(char)
#for with range()' function
for i in range(5):
    print(i)
#for with enumarate()' function
my_list = ['Apple', 'Banana', 'Orange']
for index, fruit in enumerate(my_list):
    print(f"Index: {index}, Fruit: {fruit}")

