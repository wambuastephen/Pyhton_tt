#!/usr/bin/env python3

# Assigning decimal value 10 to variable x
x = 10

# Reassigning x with binary value 0b10 (which represents 2 in decimal)
x = 0b10
print(x)         # Output: 2
print(bin(x))    # Output: '0b10' - displays the binary representation of x

# Reassigning x with hexadecimal value 0x12c (which represents 300 in decimal)
x = 0x12c
print(x)         # Output: 300
print(hex(x))    # Output: '0x12c' - displays the hexadecimal representation of x
#a + bi complex numbers
x = 1 + 2j
print(x)
