#!/usr/bin/env python3
def add(x, y):
    """add function"""
    return x + y

def subtraction(x, y):
    """subtraction function"""
    return x -y

def multiply(x, y):
    """multiply function"""
    return x * y

def divide(x, y):
    """divide function"""
    return x / y

print(add(10,5))
print(subtraction(10,5))
print(multiply(10,5))
print(divide(10,5))
